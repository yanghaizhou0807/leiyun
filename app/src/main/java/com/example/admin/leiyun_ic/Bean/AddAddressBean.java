package com.example.admin.leiyun_ic.Bean;

public class AddAddressBean {

    /**
     * code : 200
     * msg : 请求成功
     * data : 操作成功
     */

    private int code;
    private String msg;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
